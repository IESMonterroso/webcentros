                    <p>El <?php echo $config['centro_denominacion']; ?> ofrece completa la Enseñanza Secundaria Obligatoria, 1º, 2º, 3º y 4º de ESO. La Educación Secundaria Obligatoria completa la enseñanza básica y abarca cuatro cursos académicos, que se siguen ordinariamente entre los doce y los dieciséis años. Con carácter general, el alumnado tendrá derecho a permanecer escolarizado en régimen ordinario hasta los dieciocho años de edad cumplidos en el año en que finalice el curso.</p>
                    
                    <p>La normativa que regula la E.S.O. a partir del curso 2007/2008 es la L.O.E. (Ley Orgánica de la Educación) y los Decretos y Órdenes que desarrollan la misma, que en Andalucía son:</p>
                    
                    <ul>
                        <li>Decreto 231/2007 (BOJA nº 156 de 8 de agosto de 2007)</li>
                        <li>Orden de 10 de Agosto de 2007 que desarrolla el currículo(BOJA nº 171 de 30 de agosto de 2007)</li>
                        <li>Orden de 10 de Agosto de 2007 que regula la evaluación (BOJA nº 166 de 23 de agosto de 2007)</li>
                    </ul>
                    
                    <p>La finalidad de esta etapa consiste en lograr que el alumnado adquiera los elementos básicos de la cultura, especialmente en sus aspectos humanístico, artístico, científico y tecnológico; desarrollar y consolidar en él hábitos de estudio y de trabajo; prepararle para su incorporación a estudios superiores y para su inserción laboral, y formarle para el ejercicio de sus derechos y obligaciones en la vida como ciudadano o ciudadana.</p>
                    
                    <hr>
                    
                    <h4>Normas generales de ordenación de la etapa</h4>
                    
                    <p>La etapa de educación secundaria obligatoria tiene carácter obligatorio y gratuito y constituye, junto con la educación primaria, la educación básica. Comprende cuatro cursos académicos , que se seguirán ordinariamente entre los doce y los dieciséis años de edad. Con carácter general, el alumnado tendrá derecho a permanecer escolarizado en régimen ordinario hasta los dieciocho años de edad cumplidos en el año que finalice el curso. </p>
                    
                    <p>La educación secundaria obligatoria se organiza en diferentes materias. El cuarto curso tendrá carácter orientador, tanto para los estudios post-obligatorios como para la incorporación a la vida laboral.</p>
                    
                    <p>La educación secundaria obligatoria se organizará de acuerdo a los principios de educación común y de atención a a la diversidad del alumnado.</p>
                    
                    <p>A tales efectos, se pondrá especial énfasis en la adquisición de las competencias básicas, en la detección y tratamiento de las dificultades de aprendizaje tan pronto como se produzcan, en la tutoría y orientación educativa del alumnado y en la relación con las familias para apoyar el proceso educativo de sus hijos e hijas.</p>
                    
                    <p>La educación secundaria obligatoria se coordinará con la educación primaria y con las etapas posteriores del sistema educativo, con objeto de garantizar una adecuada transición del alumnado entre ellas y facilitar la continuidad de su proceso educativo.</p>
                    
                    
                    <p>La acción educativa en la educación secundaria obligatoria procurará la integración de las distintas experiencias y aprendizajes del alumnado y se adaptará a sus ritmos de trabajo.</p>
                    
                    <p>La acción educativa en la educación secundaria obligatoria prestará especial atención a aquellas áreas de conocimiento que poseen un carácter instrumental.</p>
                    
                    <hr>
                    
                    <h4>Evaluación</h4>
                    
                    <p>La evaluación del proceso de aprendizaje del alumnado será continua y diferenciada según las distintas materias del currículo.</p>
                    <p>Dicha evaluación se llevará a cabo por el profesorado, teniendo en cuenta los diferentes elementos del currículo, preferentemente a través de la observación continuada de la evolución del proceso de aprendizaje de cada alumno o alumna y de su maduración personal, sin perjuicio de las pruebas que, en su caso, realice el alumnado. En todo caso, los criterios de evaluación de las materias serán referente fundamental para valorar tanto el grado de adquisición de las competencias básicas como el de consecución de los objetivos.</p> 
                    
                    <p>El equipo docente, coordinado por quien ejerza la tutoría, actuará de manera colegiada a lo largo del proceso de evaluación y en la adopción de las decisiones resultantes del mismo.</p>
                    
                    <p>En el proceso de evaluación continua, cuando el progreso de un alumno o alumna no sea el adecuado, se establecerán medidas de refuerzo educativo. Estas medidas se adoptarán en cualquier momento del curso, tan pronto como se detecten las dificultades y estarán dirigidas a garantizar la adquisición de los aprendizajes imprescindibles para continuar el proceso educativo.</p>
                    
                    <p>Asimismo, la Consejería competente en materia de educación establecerá por Orden la obligación del profesorado de evaluar tanto los aprendizajes del alumnado como los procesos de enseñanza y su propia práctica docente.</p>
                    
                    <hr>
                    
                    <h4>Promoción del alumnado</h4>
                    
                    <p>Al finalizar cada uno de los cursos de la etapa y como consecuencia del proceso de evaluación, el equipo docente tomará las decisiones correspondientes sobre la promoción del alumnado al curso siguiente.</p>
                    
                    <p>Las decisiones sobre la promoción del alumnado de un curso a otro dentro de la etapa serán adoptadas de forma colegiada por el equipo docente, con el asesoramiento del departamento de orientación, atendiendo a la adquisición de las competencias básicas y a la consecución de los objetivos. Los centros establecerán en sus proyectos educativos la forma en que el alumno o la alumna y su padre, madre o tutores legales puedan ser oídos.</p>
                    
                    <p>Se promocionará al curso siguiente cuando se hayan superado los objetivos de las materias cursadas o se tenga evaluación negativa en dos materias como máximo, y se repetirá curso con evaluación negativa en tres o más materias. Excepcionalmente, podrá autorizarse la promoción con evaluación negativa en tres materias cuando el equipo docente considere que la naturaleza de sus dificultades no le impide seguir con éxito el curso siguiente, que tiene expectativas favorables de recuperación, a partir de las competencias básicas alcanzadas, y que dicha promoción beneficiará su evolución académica.</p>
                    
                    <p>Quien promocione sin haber superado todas las materias seguirá un programa de refuerzo destinado a la recuperación de los aprendizajes no adquiridos y deberá superar la evaluación correspondiente a dicho programa. Esta circunstancia será tenida en cuenta a los efectos de calificación de las materias no superadas, así como a los de promoción y, en su caso, obtención de la titulación prevista en el artículo 16.</p>
                    
                    <p>Corresponde a los departamentos didácticos la organización de estos programas. De su contenido se informará al alumnado y a sus padres, madres o tutores al comienzo del curso escolar.</p>
                    
                    <p>Quien no promocione deberá permanecer un año más en el mismo curso. Esta medida deberá ir acompañada de un plan específico personalizado, orientado a la superación de las dificultades detectadas en el curso anterior. Los centros docentes organizarán este plan de acuerdo con lo que, a tales efectos, establezca por Orden la Consejería competente en materia de educación.</p>
                    
                    <p>El alumnado podrá repetir el mismo curso una sola vez y dos veces como máximo dentro de la etapa. Excepcionalmente podrá repetir una segunda vez en cuarto curso si no ha repetido en cursos anteriores de la etapa. Cuando la segunda repetición deba producirse en el último curso de la etapa, se prolongará un año el límite de edad establecido en el artículo 2.a).</p>
                    
                    <p>Con el fin de facilitar al alumnado la recuperación de las materias con evaluación negativa, por Orden de la Consejería competente en materia de educación se determinarán las condiciones y se regulará el procedimiento para que los centros docentes organicen en el mes de septiembre las oportunas pruebas extraordinarias en cada uno de los cursos.</p>
                    
                    <hr>
                    
                    <h4>Titulación</h4>
                    
                    <p>El alumnado que supere todas las materias de la etapa obtendrá el título de Graduado en Educación Secundaria Obligatoria. Asimismo, podrán obtener dicho título aquellos alumnos o alumnas que hayan finalizado el curso con evaluación negativa en una o dos materias, y excepcionalmente en tres, siempre que el equipo docente considere que la naturaleza y el peso de las mismas en el conjunto de la etapa no les ha impedido alcanzar las competencias básicas y los objetivos de la etapa.</p>
                    
                    <p>La decisión sobre la obtención de la titulación será adoptada de forma colegiada por el equipo docente, con el asesoramiento del departamento de orientación. Los centros establecerán en sus proyectos educativos la forma en que el alumno o la alumna y su padre, madre o tutores legales puedan ser oídos.</p>
                    
                    <p>El alumnado que curse programas de diversificación curricular obtendrá el título de Graduado en Educación Secundaria Obligatoria si supera todos los ámbitos y materias que integran el programa. Asimismo, podrán obtener dicho título aquellos alumnos y alumnas que, habiendo superado los dos ámbitos, tengan evaluación negativa en una o dos materias, y excepcionalmente en tres, siempre que a juicio del equipo docente hayan alcanzado las competencias básicas y los objetivos de la etapa.</p>
                    
                    <p>El alumnado que al finalizar la etapa no haya obtenido el título de Graduado en Educación Secundaria Obligatoria y tenga la edad máxima de escolarización dispondrá, durante los dos años siguientes, de una convocatoria anual de pruebas para superar aquellas materias pendientes de calificación positiva, de conformidad con lo que, a tales efectos, establezca por Orden la Consejería competente en materia de educación, siempre que el número de éstas no sea superior a cinco.</p>
                    
                    <p>El alumnado que curse la educación secundaria obligatoria y no obtenga el título recibirá un certificado de escolaridad en el que consten los años y materias cursados.</p>
                    
